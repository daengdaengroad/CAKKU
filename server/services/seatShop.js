// 시트/실내/가죽 복원 관련 업체만 골라내기 위한 필터.
// - 이름에 시트 신호가 있으면 무조건 포함(가장 강한 신호)
// - 카테고리만 시트/인테리어인 경우, 이름에 다른 전문분야(오디오·썬팅 등)가
//   있으면 제외 (예: "경음카오디오"는 카테고리가 카인테리어여도 제외)
const SEAT_HINTS = [
  '시트',
  '카인테리어',
  '인테리어',
  '가죽',
  '레자',
  '레쟈',
  '실내',
  '카시트',
  '알칸타라',
  '스웨이드',
];

// 이름에 이 단어가 있으면 시트 전문점이 아니라고 보고 제외
const EXCLUDE_HINTS = [
  '오디오',
  '음향',
  '사운드',
  '하이파이',
  '앰프',
  '스피커',
  '내비',
  '네비',
  '네비게이션',
  '블랙박스',
  '블박',
  '썬팅',
  '틴팅',
  '유리막',
  '판금',
  '도색',
  '휠',
  '타이어',
  '엔진오일',
  '세차',
  '카매트',
];

function isSeatShop(name = '', category = '') {
  // 이름에 다른 전문분야(오디오·타이어·세차 등)가 명시되면 우선 제외
  if (EXCLUDE_HINTS.some((k) => name.includes(k))) return false;

  // 이름 또는 카테고리에 시트/실내 신호가 있으면 포함
  const hay = `${name} ${category}`;
  return SEAT_HINTS.some((k) => hay.includes(k));
}

module.exports = { isSeatShop };
